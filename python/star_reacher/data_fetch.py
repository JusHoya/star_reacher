"""``star data fetch`` implementation: dataset download and repack (D-8, FR-4, FR-5).

One-time maintainer-side pipelines, one per dataset:

- ``de440s``: download the JPL DE440s planetary SPK and the DE440 lunar
  principal-axis binary PCK, verify both against pinned SHA-256 values, and
  repack the segments star_reacher needs (Sun, EMB, Earth, Moon, Venus
  barycenter, Mars barycenter, Jupiter barycenter, lunar libration angles;
  2020-01-01 through 2060-01-01 TDB) into one small SREPH v1 binary file that
  the C++ core loads directly. The Chebyshev coefficients are copied verbatim
  from the source kernels - never refitted - so the repack inherits DE440
  accuracy exactly (format specification: ``docs/formats/sreph_v1.md``).
- ``egm2008`` / ``grgm1200a`` / ``mro120f`` (FR-5): download the published
  spherical-harmonic gravity coefficient file for Earth, Moon, or Mars,
  verify it against a pinned SHA-256, and repack the fully normalized
  C-bar/S-bar coefficients (truncated to the FR-5 degree: 70x70, 120x120,
  80x80 respectively) together with the source file's own GM and reference
  radius into one small SRGRAV v1 binary file for the C++ core (format
  specification: ``docs/formats/srgrav_v1.md``).

The module also carries pure-Python SREPH/SRGRAV readers; the SREPH reader
doubles as the executable specification of the C++ Chebyshev evaluator
(``cpp/src/ephemeris.cpp``): both implement the identical operation sequence,
so on IEEE-754 double hardware with FMA contraction disabled (D-10 build
flags) they produce bit-identical outputs. The committed bit-level golden
vectors under ``tests/golden/ephemeris/`` are generated by this evaluator.

Everything here runs before t0 (D-2 boundary rule): network, text, and
checksum handling stay in Python; the core only ever sees the binary SREPH
and SRGRAV files.
"""

