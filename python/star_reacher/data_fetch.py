def fetch_gravity(dataset: str, data_dir: Path) -> Path:
    """Fetch and repack one FR-5 gravity field; idempotent. Returns the repack path."""
    spec = GRAVITY_DATASETS[dataset]
    data_dir = Path(data_dir)
    data_dir.mkdir(parents=True, exist_ok=True)
    source_path = data_dir / spec.source_filename
    out_path = data_dir / f"{spec.dataset}_n{spec.n_repack}.srgrav"
    manifest_path = data_dir / f"{spec.dataset}_manifest.json"

    source_sha = _ensure_source(spec.url, spec.source_sha256, source_path)

    if out_path.is_file() and manifest_path.is_file():
        try:
            manifest = json.loads(manifest_path.read_text(encoding="utf-8"))
        except (OSError, json.JSONDecodeError):
            manifest = {}
        recorded = manifest.get("repack_sha256")
        if recorded and _sha256(out_path) == recorded:
            print(f"{out_path.name}: present, SHA-256 verified ({recorded[:12]}...)")
            print(f"data fetch {dataset}: verified, nothing to do ({out_path})")
            return out_path

    if spec.source_format == "icgem_gfc":
        field = parse_icgem_gfc(source_path, spec.n_repack, source_sha)
        field.name = spec.field_name
    else:
        field = parse_pds_shadr(
            source_path, spec.n_repack, spec.field_name, spec.tide_system or "unknown", source_sha
        )
    if spec.tide_system is not None:
        field.tide_system = spec.tide_system
    write_srgrav(out_path, field)
    repack_sha = _sha256(out_path)
    manifest_path.write_text(
        json.dumps(
            {
                "dataset": spec.dataset,
                "format": "SRGRAV",
                "format_version": f"{SRGRAV_VERSION_MAJOR}.{SRGRAV_VERSION_MINOR}",
                "body": spec.body,
                "field_name": spec.field_name,
                "citation": spec.citation,
                "sources": [{"url": spec.url, "sha256": source_sha}],
                "repack_file": out_path.name,
                "repack_sha256": repack_sha,
                "n_max": field.n_max,
                "m_max": field.m_max,
                "gm_m3ps2": field.gm_m3ps2,
                "ref_radius_m": field.ref_radius_m,
                "tide_system": field.tide_system,
            },
            indent=2,
        )
        + "\n",
        encoding="utf-8",
    )
    size_kib = out_path.stat().st_size / 1024.0
    print(
        f"repacked {spec.field_name} to {field.n_max}x{field.m_max}, "
        f"{size_kib:.0f} KiB -> {out_path}"
    )
    print(f"data fetch {dataset}: complete ({out_path})")
    return out_path


def cli_fetch(dataset: str, data_dir: str) -> int:
    """CLI entry: ``star data fetch <dataset>``. Returns the exit code."""
    known = ["de440s", *sorted(GRAVITY_DATASETS)]
    if dataset not in known:
        print(
            f"star data fetch: unknown dataset {dataset!r}; available: "
            f"{', '.join(known)}.",
            file=sys.stderr,
        )
        return 2
    try:
        if dataset == "de440s":
            fetch_de440s(Path(data_dir))
        else:
            fetch_gravity(dataset, Path(data_dir))
    except DataFetchError as exc:
        print(f"star data fetch: {exc}", file=sys.stderr)
        return 1
    except OSError as exc:
        print(f"star data fetch: {exc}", file=sys.stderr)
        return 1
    return 0
