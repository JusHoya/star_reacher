// Series tables for the IAU 2006/2000B Earth-orientation chain
// (cpp/src/frames.cpp). Implementation detail, not part of the public
// star/ include surface; the doctest suite includes this header directly
// to check every value against tests/golden/frames/series_terms.toml
// (transcription-equality gate, test frames_series_transcription).
//
// Sources, transcribed verbatim from the ERFA 2.0.1 reference source
// (liberfa/erfa), which implements the cited publications:
// - 77-term IAU 2000B luni-solar nutation series and planetary-bias
//   offsets: McCarthy & Luzum, Cel. Mech. Dyn. Astron. 85, 37-49 (2003);
//   offsets per Luzum (2001), the "rigorous method" values (nut00b.c).
// - s(X,Y) series: IERS Conventions (2010) Chapter 5, SOFA S06 form
//   (s06.c).
// - IAU 2006 (P03) Fukushima-Williams bias-precession angle polynomials
//   and mean obliquity: Capitaine, Wallace & Chapront, Astron. Astrophys.
//   412, 567-586 (2003); Wallace & Capitaine, Astron. Astrophys. 459,
//   981-985 (2006) (pfw06.c, obl06.c).
// - Fundamental-argument polynomials: IERS Conventions (2003/2010)
//   (fal03.c .. fapa03.c).
// - Earth rotation angle constants: Capitaine, Guinot & McCarthy, Astron.
//   Astrophys. 355, 398-405 (2000); IERS Conventions (2010) eq. 5.15
//   (era00.c).
